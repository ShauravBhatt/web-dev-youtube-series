/* Post Decrement & Pre Decrement */
    // Mathematical operation consists of Two Things 1.) Operands 2.) Operator
    // Post Means (After) , Pre Means (Before)
    // Post Says First Use Me Then I Update , Pre Says First Update Me Then Use

let num = 7 ;
console.log(num) ; //7

console.log(num++); //print 7 & update it to 8
console.log(num--); //print 8 & update it to 7

console.log(++num); // Update it to 8 & then print 8 
console.log(--num); // Update it to 7 first then print 7






